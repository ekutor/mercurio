    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
        try {
            double monto = Utilidades.quitarFormatoNumero(txtValor.getText());
            if (monto > 0) {
                FachadaInterfaz.getInstancia().establecerBaseCaja(monto);
                this.dispose();
            } else {
                new DialogoError("Solo se permiten valores reales", null);
            }
        } catch (java.lang.NumberFormatException nfe) {
            new DialogoError("Debe Ingresar el Monto Inicial de La Caja", nfe);
        }
    }//GEN-LAST:event_btnAsignarActionPerformed
