    public void setFocoPerdido(FocusEvent evt) {
        evt.getComponent().setBackground(FOCO_PERDIDO);
        if(evt.getSource().equals(this.txtValor)){
            String str = txtValor.getText();
            txtValor.setText(Utilidades.darFormatoEspecial(Integer.parseInt(Utilidades.contenidoCajaTexto(str))));
            this.btnAsignar.requestFocus(true);
        }
    }
